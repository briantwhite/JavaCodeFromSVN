    private void jButtonChooseActionPerformed(java.awt.event.ActionEvent evt) throws Exception {//GEN-FIRST:event_jButtonChooseActionPerformed
        // Button Choose
    	
        String courseName = (String)jList3.getSelectedValue();
        if (courseName == null) return;
        
        test.LoadCourse(courseName);
        test.getStartSession().setCourseName(courseName);
        this.setVisible(false);
        test.getStartSession().setVisible(true);
    }//GEN-LAST:event_jButtonChooseActionPerformed

