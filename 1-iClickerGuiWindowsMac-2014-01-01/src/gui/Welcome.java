    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        // Button Edit
    	
        String editName = (String)jList3.getSelectedValue();
        if (editName == null) return;
        
        test.getEditCourse().setFields(editName);
        SetLocation.setCenterParent(this, test.getEditCourse());
        test.getEditCourse().setVisible(true);
    }//GEN-LAST:event_jButtonEditActionPerformed

